import math

a = float(input("Nhập số thứ nhất: "))
b = float(input("Nhập số thứ hai: "))

# Lũy thừa
print("a^b =", a**b)

# Căn bậc 2
print("Căn bậc 2 của a =", math.sqrt(a))
print("Căn bậc 2 của b =", math.sqrt(b))

# Chia lấy phần nguyên
print("a // b =", a // b)

# Chia lấy phần dư
print("a % b =", a % b)

# Làm tròn
print("Làm tròn a =", round(a))
print("Làm tròn b =", round(b))